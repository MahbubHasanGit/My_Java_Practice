public class Variable_Declaration {
    public static void main(String[] args) {
   int int_number = 10 ;
   float float_number = 10.50f ;
   double double_number = 10.12354 ;
   char character = 'm' ;
   String string = "Mahbub Hasan" ;
   
   System.out.println("The integer number is "+int_number);
   System.out.println("The float number is "+float_number);
   System.out.println("The double number is "+double_number);
   System.out.println("The character is "+character);
   System.out.println("The string is "+string);
}
    
}
